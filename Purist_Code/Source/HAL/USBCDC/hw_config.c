/*******************************************************************************
* Function Name  : USB_To_USART_Send_Data.
* Description       :  Save data received from usb port & prepare to send to UART(or transfer to other peripherals).
* Input              : data_buffer , data buffer 
                          Nb_bytes , data length.
* Output            : None.
* Return             : None.
*******************************************************************************/
void USB_To_USART_Send_Data(uint8_t* data_buffer, uint8_t Nb_bytes)
{
  
  uint32_t i;
  
  for (i = 0; i < Nb_bytes; i++)
  {
    USART_Tx_Buffer[USART_Tx_ptr_in] = *(data_buffer + i);
    USART_Tx_ptr_in++;
    if(USART_Tx_ptr_in >= USART_TX_DATA_SIZE) // ylf: turn around
        USART_Tx_ptr_in = 0;
  } 
  /* Add Callback Here */
  if (sUsb2UartCb) (*sUsb2UartCb)(0XFF,HAL_UART_RX_TIMEOUT);
}

/*******************************************************************************
* Function Name  : Handle_USBAsynchXfer.
* Description    : send data to USB.
* Input          : None.
* Return         : none.
*******************************************************************************/
void Handle_USBAsynchXfer (void)
{
  
  uint16_t USB_Tx_ptr;
  uint16_t USB_Tx_length;
  
  if(USB_Tx_State != 1)
  {
    if (USART_Rx_ptr_out == USART_RX_DATA_SIZE)
    {
      USART_Rx_ptr_out = 0;
    }
    
    if(USART_Rx_ptr_out == USART_Rx_ptr_in) 
    {
      USB_Tx_State = 0; 
      return;
    }

    USB_Tx_ptr = USART_Rx_ptr_out;
    
    if(USART_Rx_ptr_out > USART_Rx_ptr_in) /* rollback */
    { 
      USART_Rx_length = USART_RX_DATA_SIZE - USART_Rx_ptr_out + USART_Rx_ptr_in;

      USB_Tx_length = USART_RX_DATA_SIZE - USART_Rx_ptr_out;
    }
    else 
    {
      USART_Rx_length = USART_Rx_ptr_in - USART_Rx_ptr_out;

      USB_Tx_length = USART_Rx_length;
    }

    
    if (USB_Tx_length > VIRTUAL_COM_PORT_DATA_SIZE)
    {
      USB_Tx_length = VIRTUAL_COM_PORT_DATA_SIZE;
      
    }

    USART_Rx_ptr_out = (USART_Rx_ptr_out + USB_Tx_length)%USART_RX_DATA_SIZE;
    
    USART_Rx_length -= USB_Tx_length;
    
    USB_Tx_State = 1; 
    
#ifdef STM32F10X_CL
    USB_SIL_Write(EP1_IN, &USART_Rx_Buffer[USB_Tx_ptr], USB_Tx_length);  
#else
    UserToPMABufferCopy(&USART_Rx_Buffer[USB_Tx_ptr], ENDP1_TXADDR, USB_Tx_length);
    SetEPTxCount(ENDP1, USB_Tx_length);
    SetEPTxValid(ENDP1); 
#endif
  }  
  
}

/*******************************************************************************
* Function Name  : HexToChar.
* Description    : Convert Hex 32Bits value into char.
* Input          : None.
* Output         : None.
* Return         : None.
*******************************************************************************/
static void IntToUnicode (uint32_t value , uint8_t *pbuf , uint8_t len)
{
  uint8_t idx = 0;
  
  for( idx = 0 ; idx < len ; idx ++)
  {
    if( ((value >> 28)) < 0xA )
    {
      pbuf[ 2* idx] = (value >> 28) + '0';
    }
    else
    {
      pbuf[2* idx] = (value >> 28) + 'A' - 10; 
    }
    
    value = value << 4;
    
    pbuf[ 2* idx + 1] = 0;
  }
}

